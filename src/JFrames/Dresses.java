    public void autoGenItemNum() //Function for autogenerate the Dress ID
                    
    {       
        try {
            DBConnector dbCon = new DBConnector();
            Connection con = dbCon.getConnection();
              
            Statement stmt = con.createStatement();   
            
            ResultSet rs = stmt.executeQuery("Select Max(dress_code)from dress");
            rs.next();
            rs.getString( "Max(dress_code)");
            
            if(rs.getString( "Max(dress_code)") == null)
            {
                txtDressCode.setText("D001");
            }
            else
            {
                long id = Long.parseLong(rs.getString( "Max(dress_code)").substring(2,rs.getString("Max(dress_code)").length()));
                id++;
                
                txtDressCode.setText("D" + String.format("%03d",id));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Dresses.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }
    
    
