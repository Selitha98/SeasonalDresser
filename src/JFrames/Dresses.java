    public void update_table()
    {
        int c;
        
         try {
            DBConnector dbCon = new DBConnector();
            Connection con = dbCon.getConnection();
           
            PreparedStatement pst = null;
            pst = con.prepareStatement("SELECT * FROM `dress`");
            ResultSet Rs  = pst.executeQuery();
            
            ResultSetMetaData rd = Rs.getMetaData();
            c = rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jtableDress.getModel();
            df.setRowCount(0);
            
            while(Rs.next())
            {
                Vector v2 = new Vector();
                for (int i=1; i<=c; i++)
                {
                   v2.add(Rs.getString("dress_code"));
                   v2.add(Rs.getString("dress_name"));
                   v2.add(Rs.getString("category"));
                   v2.add(Rs.getString("size"));
                   v2.add(Rs.getInt("quantity"));
                   v2.add(Rs.getString("colour"));
                   v2.add(Rs.getInt("price"));
                   v2.add(Rs.getBytes("image"));
                }
                
                df.addRow(v2);
                
            }
          
            
        } catch (SQLException ex) {
            Logger.getLogger(Dresses.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
