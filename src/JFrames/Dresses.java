    public boolean checkDressCode(String DCodecheck) //Function to avoid Duplicate entry of Dress ID
    {
       PreparedStatement st;
       ResultSet rs;
       boolean check_DressCode = false;
       String query = "SELECT * FROM `dress` WHERE `dress_code` =?";
       
        try 
        {
            st = DBConnector.getConnection().prepareStatement(query);
            st.setString(1,DCodecheck);
            
            rs = st.executeQuery();
            
            if(rs.next())
            {
                check_DressCode = true;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Dresses.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        return check_DressCode;
       
    }
    
    
