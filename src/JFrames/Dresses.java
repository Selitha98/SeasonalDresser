    private void btnAddDressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDressActionPerformed
        // Add dress button codes

        String Dcode = txtDressCode.getText();
        String Dname = txtDressName.getText();
        String Dcategory = cmbCategory.getSelectedItem().toString();
        String Dsize = cmbSize.getSelectedItem().toString();
        Integer Dqty = Integer.parseInt(txtQuantity.getText());
        String Dcolor = txtColor.getText();
        Integer  Dprice = Integer.parseInt(txtPrice.getText());
        
        /* *********************************************************/
        /*            Validation for the empty fields             **/ 
        /* *********************************************************/ 

        if(Dname.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Insert Dress name");
        }
        else if(Dqty.equals(0))
        {
            JOptionPane.showMessageDialog(null, "Enter Dress Quantity");
        }

        else if(Dcolor.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Insert Dress color");
        }

        else if(Dprice.equals(0))
        {
            JOptionPane.showMessageDialog(null, "Insert a Price");
        }
        
        else if(checkDressCode(Dcode))
        {
            JOptionPane.showMessageDialog(null," Dress is Already Exist for this Code"); // check exiasting emails
        }

      
        else {

            try {
                DBConnector dbCon = new DBConnector();
                Connection con = dbCon.getConnection();

                PreparedStatement pst = null;
                pst = con.prepareStatement("INSERT INTO `dress`(`dress_code`, `dress_name`, `category`, `size`,`quantity`, `colour`, `price`, `image`) VALUES (?,?,?,?,?,?,?,?)");

                pst.setString(1,Dcode);
                pst.setString(2, Dname);
                pst.setString(3, Dcategory);
                pst.setString(4, Dsize);
                pst.setInt(5, Dqty);
                pst.setString(6, Dcolor);
                pst.setInt(7, Dprice);
                pst.setBytes(8, photo);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this,"New Dress Added");
                
         /* *********************************************************/
        /*   Clearing the text fields after adding a new record    */ 
        /* *********************************************************/

                txtDressName.setText("");
                cmbCategory.setSelectedIndex(-1);
                cmbSize.setSelectedIndex(-1);
                txtColor.setText("");
                txtPrice.setText("");
                txtQuantity.setText("");
                txtDressName.requestFocus();
                
                autoGenItemNum(); //function call for Auto number
                update_table(); //Function call for Update the table

            }
            catch (SQLException ex) {
                Logger.getLogger(Dresses.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnAddDressActionPerformed

